# Various examples for lists

our_list = [1, 13, 5, -3, 99]
print("The list is {}".format(our_list))
print(type(our_list))

# Pull data from list 
print(our_list[4])

# Pull multiple data

print(our_list[0:3])


# Lists inside lists, like tables

our_table = [ [1.2,3],[ 4,5,6],[7,8,9] ]
print(our_table)

print(our_table[2])
print(our_table[2][1])
